const add = (a, b) => {
  const result = a + b;
  console.log(`${a} + ${b} = ${result}`);
};

const subtract = (a, b) => {
  const result = a - b;
  console.log(`${a} - ${b} = ${result}`);
};

const multiply = (a, b) => {
  const result = a * b;
  console.log(`${a} * ${b} = ${result}`);
};

const divide = (a, b) => {
  const result = a / b;
  console.log(`${a} / ${b} = ${result}`);
};

add(565, 584);
subtract(474, 520);
multiply(5822, 65);
divide(632, 6);
